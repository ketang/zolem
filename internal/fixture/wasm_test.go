package fixture_test

import (
	"context"
	"testing"

	"github.com/ketang/zolem/internal/fixture"
)

// alwaysMatchWASM is a compiled WASM module equivalent to:
//
//	(module
//	  (memory (export "memory") 1)
//	  (func (export "match") (param i32 i32) (result f32)
//	    f32.const 1.0
//	  )
//	)
//
// wazero v1.11.0 requires binary WASM; WAT text is not accepted by CompileModule.
var alwaysMatchWASM = []byte{
	0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00,
	0x01, 0x07, 0x01, 0x60, 0x02, 0x7f, 0x7f, 0x01,
	0x7d, 0x03, 0x02, 0x01, 0x00, 0x05, 0x03, 0x01,
	0x00, 0x01, 0x07, 0x12, 0x02, 0x06, 0x6d, 0x65,
	0x6d, 0x6f, 0x72, 0x79, 0x02, 0x00, 0x05, 0x6d,
	0x61, 0x74, 0x63, 0x68, 0x00, 0x00, 0x0a, 0x09,
	0x01, 0x07, 0x00, 0x43, 0x00, 0x00, 0x80, 0x3f, 0x0b,
}

// noMatchWASM is a compiled WASM module equivalent to:
//
//	(module
//	  (memory (export "memory") 1)
//	  (func (export "match") (param i32 i32) (result f32)
//	    f32.const -1.0
//	  )
//	)
var noMatchWASM = []byte{
	0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00,
	0x01, 0x07, 0x01, 0x60, 0x02, 0x7f, 0x7f, 0x01,
	0x7d, 0x03, 0x02, 0x01, 0x00, 0x05, 0x03, 0x01,
	0x00, 0x01, 0x07, 0x12, 0x02, 0x06, 0x6d, 0x65,
	0x6d, 0x6f, 0x72, 0x79, 0x02, 0x00, 0x05, 0x6d,
	0x61, 0x74, 0x63, 0x68, 0x00, 0x00, 0x0a, 0x09,
	0x01, 0x07, 0x00, 0x43, 0x00, 0x00, 0x80, 0xbf, 0x0b,
}

func TestRunner_Score_AlwaysMatch(t *testing.T) {
	r := fixture.NewRunner()
	defer r.Close()

	mod, err := r.CompileWASM(context.Background(), alwaysMatchWASM)
	if err != nil {
		t.Fatalf("compile: %v", err)
	}

	score, err := r.Score(context.Background(), mod, []byte(`{"provider":"anthropic"}`))
	if err != nil {
		t.Fatalf("score: %v", err)
	}
	if score < 0 {
		t.Errorf("expected non-negative score, got %f", score)
	}
}

func TestRunner_Score_NoMatch(t *testing.T) {
	r := fixture.NewRunner()
	defer r.Close()

	mod, err := r.CompileWASM(context.Background(), noMatchWASM)
	if err != nil {
		t.Fatalf("compile: %v", err)
	}

	score, err := r.Score(context.Background(), mod, []byte(`{}`))
	if err != nil {
		t.Fatalf("score: %v", err)
	}
	if score >= 0 {
		t.Errorf("expected negative score, got %f", score)
	}
}
