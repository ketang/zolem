func TestGenerator_GenerateChunks(t *testing.T) {
	g, err := wasmgen.Compile(generatorWASM, 100*time.Millisecond)
	if err != nil {
		t.Fatalf("compile: %v", err)
	}
	defer g.Close(context.Background())

	chunks, err := g.Generate(context.Background(), fixture.MatchRequest{
		Provider: "openai",
		Version:  "v1",
		Body:     []byte(`{"messages":[{"role":"user","content":"hi"}]}`),
	})
	if err != nil {
		t.Fatalf("generate: %v", err)
	}
	if got := strings.Join(chunks, ""); got != "Hello from WASM." {
		t.Fatalf("chunks: got %q", got)
	}
}
